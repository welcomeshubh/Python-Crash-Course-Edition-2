# You can add (+), subtract (-), multiply (*), and divide (/) integers in Python.

#ADDITION
print( 2 + 3 )

#SUBTRACTION
print( 3 - 2 )

#MULTIPLICATION
print( 2 * 3 )

#DIVISION
print( 3 / 2 )

#EXPONENT
print( 3 ** 2 )
print( 3 ** 3 )
print( 10 ** 6 )

#ORDER OF OPERATION
print( 2 + 3 * 4 )
print( ( 2 + 3 ) * 4 )

#FLOATING POINT NUMBERS

print(0.1 + 0.1)
print(0.2 + 0.2)
print(2 * 0.1)
print(2 * 0.2)

 # be aware that you can sometimes get an arbitrary number of decimal places in your answer:
print(0.2 + 0.1)
print(3 * 0.1)



#INTEGERS AND FLOATS

print( 4 / 2 )
print( 1 + 2.0 )
print(2 * 3.0)
print(3.0 ** 2)


#UNDERSCORES IN NUMBERS
universal_age = 14_000_000_000
print(universal_age)


#MULTIPE ASSIGNMENTS
x,y,z = 0,1,2
print(x)
print(y)
print(z)



# CONSTANTS
MAX_CONNECTIONS = 5000
print(MAX_CONNECTIONS)


